#ifndef LLVM_LIB_TARGET_LAIN_LAINSUBTARGET_H
#define LLVM_LIB_TARGET_LAIN_LAINSUBTARGET_H

#include "LAIN.h"
#include "LAINFrameLowering.h"
#include "LAINISelLowering.h"
#include "LAINInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "LAINGenSubtargetInfo.inc"


namespace llvm {

class StringRef;
class TargetMachine;

class LAINSubtarget : public LAINGenSubtargetInfo {
  virtual void anchor();

  LAINRegisterInfo RegInfo;
  LAINInstrInfo InstrInfo;
  LAINFrameLowering FrameLowering;
  LAINTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
public:
  LAINSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);


  const LAINInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const LAINFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const LAINTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const LAINRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_LAIN_LAINSUBTARGET_H